<template>
  <a-spin :spinning="confirmLoading">
    <j-form-container :disabled="formDisabled">
      <a-form-model ref="form" :model="model" :rules="validatorRules" slot="detail">
        <a-row>
          <a-col :span="24">
            <a-form-model-item label="空间群组" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="spacegroup">
              <a-input-number v-model="model.spacegroup" placeholder="请输入空间群组" style="width: 100%" />
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="弥勒指数" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="millerIndex">
              <a-input v-model="model.millerIndex" placeholder="请输入弥勒指数"  ></a-input>
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="晶系" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="valenceElectrons">
              <a-input-number v-model="model.valenceElectrons" placeholder="请输入晶系" style="width: 100%" />
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="原子半径" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="atomicRadius">
              <a-input-number v-model="model.atomicRadius" placeholder="请输入原子半径" style="width: 100%" />
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="表面能" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="surfaceEnergy">
              <a-input-number v-model="model.surfaceEnergy" placeholder="请输入表面能" style="width: 100%" />
            </a-form-model-item>
          </a-col>
          <a-col :span="24">
            <a-form-model-item label="功函数" :labelCol="labelCol" :wrapperCol="wrapperCol" prop="workFunction">
              <a-input-number v-model="model.workFunction" placeholder="请输入功函数" style="width: 100%" />
            </a-form-model-item>
          </a-col>
        </a-row>
      </a-form-model>
    </j-form-container>
  </a-spin>
</template>

<script>

  import { httpAction, getAction } from '@/api/manage'
  import { validateDuplicateValue } from '@/utils/util'

  export default {
    name: 'MetalFinalForm',
    components: {
    },
    props: {
      //表单禁用
      disabled: {
        type: Boolean,
        default: false,
        required: false
      }
    },
    data () {
      return {
        model:{
         },
        labelCol: {
          xs: { span: 24 },
          sm: { span: 5 },
        },
        wrapperCol: {
          xs: { span: 24 },
          sm: { span: 16 },
        },
        confirmLoading: false,
        validatorRules: {
        },
        url: {
          add: "/dem/metalFinal/add",
          edit: "/dem/metalFinal/edit",
          queryById: "/dem/metalFinal/queryById"
        }
      }
    },
    computed: {
      formDisabled(){
        return this.disabled
      },
    },
    created () {
       //备份model原始值
      this.modelDefault = JSON.parse(JSON.stringify(this.model));
    },
    methods: {
      add () {
        this.edit(this.modelDefault);
      },
      edit (record) {
        this.model = Object.assign({}, record);
        this.visible = true;
      },
      submitForm () {
        const that = this;
        // 触发表单验证
        this.$refs.form.validate(valid => {
          if (valid) {
            that.confirmLoading = true;
            let httpurl = '';
            let method = '';
            if(!this.model.id){
              httpurl+=this.url.add;
              method = 'post';
            }else{
              httpurl+=this.url.edit;
               method = 'put';
            }
            httpAction(httpurl,this.model,method).then((res)=>{
              if(res.success){
                that.$message.success(res.message);
                that.$emit('ok');
              }else{
                that.$message.warning(res.message);
              }
            }).finally(() => {
              that.confirmLoading = false;
            })
          }
         
        })
      },
    }
  }
</script>